#~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~#
# 目的関数の定義
function fitness(x::Vector{Float64})::Float64
    sum_val = objective_function(x)

    return sum_val >= 0 ? 1.0 / (1.0 + sum_val) : abs(1.0 + sum_val)
end

